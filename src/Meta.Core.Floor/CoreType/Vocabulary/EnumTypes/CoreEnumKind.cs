public enum CoreEnumKind
{
    None = 0,
    
    /// <summary>
    /// Identifies an enumeration with an underlying type of <see cref="sbyte"/>
    /// </summary>
    Int8 = 1,

    /// <summary>
    /// Identifies an enumeration with an underlying type of <see cref="byte"/>
    /// </summary>
    UInt8 = 2,

    /// <summary>
    /// Identifies an enumeration with an underlying type of <see cref="short"/>
    /// </summary>
    Int16 = 3,

    /// <summary>
    /// Identifies an enumeration with an underlying type of <see cref="ushort"/>
    /// </summary>
    UInt16 = 4,

    /// <summary>
    /// Identifies an enumeration with an underlying type of <see cref="int"/>
    /// </summary>
    Int32 = 5,

    /// <summary>
    /// Identifies an enumeration with an underlying type of <see cref="uint"/>
    /// </summary>
    UInt32 = 6,

    /// <summary>
    /// Identifies an enumeration with an underlying type of <see cref="long"/>
    /// </summary>
    Int64 = 7,

    /// <summary>
    /// Identifies an enumeration with an underlying type of <see cref="ulong"/>
    /// </summary>
    UInt64 = 8

}